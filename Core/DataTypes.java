public class DataTypes {
    public static void main(String[] args) {
        int num = 1 ;
        char ch = 'c' ;
        String str = "string" ;
        float ft = 1.9f ;
        double db = 4.90 ;
        boolean bl = true ;

        System.out.println(num);
        System.out.println(ch);
        System.out.println(str);
        System.out.println(ft);
        System.out.println(db);
        System.out.println(bl);
    }
}
